package net.crew_vre.recordings.layout;

import net.crew_vre.domain.DomainObject;

public class LayoutPosition extends DomainObject {

    private int x = 0;
    private int y = 0;
    private int width = 0;
    private int height = 0;
    private String name;
    private boolean assignable = true;
    private boolean changes = false;

    public LayoutPosition() {
        name = null;
    }

    public LayoutPosition(String name) {
        this.name = name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getX() {
        return x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getY() {
        return y;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getWidth() {
        return width;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getHeight() {
        return height;
    }

    public void setAssignable(boolean assignable) {
        this.assignable = assignable;
    }

    public boolean isAssignable() {
        return assignable;
    }

    public boolean equals(Object other) {
        return name.equals(((LayoutPosition) other).name);
    }

    public int hashCode() {
        return name.hashCode();
    }

    public void setChanges(boolean changes) {
        this.changes = changes;
    }

    public boolean hasChanges() {
        return changes;
    }
}
